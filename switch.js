var color = "yellow";

switch (color) {
    case "red":
        console.log("the color is red");
        break;
    case "green":
        console.log("the color is green");
        break;
    case "blue":
        console.log("the color is blue");
        break;
    case "magenta":
        console.log("the color is magenta");
        break;
    case "yellow":
        console.log("the color is yellow");
        break;

    default:
        console.log("no color found");
        
}

